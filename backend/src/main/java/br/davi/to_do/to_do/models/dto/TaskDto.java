package br.davi.to_do.to_do.models.dto;

import br.davi.to_do.to_do.models.entity.Task;
import br.davi.to_do.to_do.models.enums.TaskStatus;

public class TaskDto {
    private Long id;
    private String title;
    private String description;
    private TaskStatus status;

    public TaskDto() {
    }

    public TaskDto(Long id, String title, String description, TaskStatus status) {
        this.id = id;
        this.title = title;
        this.description = description;
        this.status = status;
    }

    public TaskDto(Task task) {
        this.id = task.getId();
        this.title = task.getTitle();
        this.description = task.getDescription();
        this.status = task.getStatus();
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public TaskStatus getStatus() {
        return status;
    }

    public void setStatus(TaskStatus status) {
        this.status = status;
    }
}
